    for i in range(len(ary)):
        if ary[i] <  -(std * sigma) or ary[i] > (std * sigma):
            # print('outlier filtered',ary[i])
            pass
        else:
            new_ary.append(ary[i])
    if fixed_len != 0:
        while len(new_ary) > fixed_len:
            new_ary.pop(random.randrange(len(new_ary)))
